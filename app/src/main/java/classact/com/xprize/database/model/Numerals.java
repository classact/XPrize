package classact.com.xprize.database.model;

public class Numerals {
    private int numeralID;
    private int languageID;
    private int number;
    private int boyGirl;
    private String sound;
    private String blackImage;
    private String sparklingImage;

    public int getNumeralID() {
        return numeralID;
    }

    public void setNumeralID(int numeralID) {
        this.numeralID = numeralID;
    }

    public int getLanguageID() {
        return languageID;
    }

    public void setLanguageID(int languageID) {
        this.languageID = languageID;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public int getBoyGirl() {
        return boyGirl;
    }

    public void setBoyGirl(int boyGirl) {
        this.boyGirl = boyGirl;
    }

    public String getSound() {
        return sound;
    }

    public void setSound(String sound) {
        this.sound = sound;
    }

    public String getBlackImage() {
        return blackImage;
    }

    public void setBlackImage(String blackImage) {
        this.blackImage = blackImage;
    }

    public String getSparklingImage() {
        return sparklingImage;
    }

    public void setSparklingImage(String sparklingImage) {
        this.sparklingImage = sparklingImage;
    }


}